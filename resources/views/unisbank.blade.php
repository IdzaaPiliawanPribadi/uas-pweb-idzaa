                                <h2>About Unisbank Semarang</h2>
                                <p class="excert">
                                    Universitas Stikubank Semarang, disingkat Unisbank, adalah Entrepreneurial University berbasis teknologi informasi dan komputer yang terletak di pusat kota Semarang.
